export default {
  eleventyComputed: {
    faq: (data) => {
      const el = data.element;
      if (!el) return [];
      return [
        {
          q: `What direction is the ${el.name} element associated with?`,
          a: `In classical five-element cosmology, ${el.name} (${el.nameTc}) is associated with the ${el.direction} direction and the ${el.season} season.`
        },
        {
          q: `Which zodiac animals belong to the ${el.name} element?`,
          a: `The zodiac animals with ${el.name} as their fixed element are: ${el.animals.join(', ')}. These animals share ${el.name}'s core qualities in their fundamental nature.`
        },
        {
          q: `What does ${el.name} generate in the Wu Xing cycle?`,
          a: `${el.name} generates ${el.generates} in the sheng (生) or generating cycle. ${el.name} is itself generated by ${el.generatedBy}.`
        },
        {
          q: `What organ does the ${el.name} element govern in TCM?`,
          a: `In Traditional Chinese Medicine, ${el.name} governs the ${el.yinOrgan} (yin organ) and the ${el.yangOrgan} (yang organ). The associated emotion is ${el.emotion} and the taste is ${el.taste}.`
        }
      ];
    }
  }
};
